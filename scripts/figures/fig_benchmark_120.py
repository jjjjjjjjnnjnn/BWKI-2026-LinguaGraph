#!/usr/bin/env python3
"""
Benchmark Visualization — 120 model ranking figure.

Consumes batch_*_results.json from data/model_comparison/
Generated by: scripts/batch_model_benchmark.py --stage full

Outputs:
  outputs/figures/fig_benchmark_120.png — Histogram of model F1 scores
  outputs/figures/fig_benchmark_120_data.csv — Full ranking table
  outputs/figures/model_benchmark_full.json — Aggregated results

Usage:
    python scripts/figures/fig_benchmark_120.py
"""

import csv, json, sys
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np

PROJECT_ROOT = Path(__file__).resolve().parents[2]
DATA_DIR = PROJECT_ROOT / "data" / "model_comparison"
OUTPUT_DIR = PROJECT_ROOT / "outputs" / "figures"
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


def load_benchmark_results() -> list[dict]:
    """Load all batch model results with valid F1 scores."""
    results = []
    if not DATA_DIR.exists():
        print("  [WARN] model_comparison directory not found")
        return results

    for fpath in sorted(DATA_DIR.glob("batch_*_results.json")):
        try:
            d = json.loads(fpath.read_text(encoding="utf-8"))
            f1 = d["summary"]["mean_f1"]
            n_valid = d.get("valid", 0)
            # Only include models with meaningful results
            if f1 > 0.0 and n_valid >= 5:
                results.append({
                    "model": d["model"],
                    "f1": f1,
                    "precision": d["summary"]["mean_precision"],
                    "recall": d["summary"]["mean_recall"],
                    "valid": n_valid,
                    "total": d["total_items"],
                    "failed": d.get("failed", 0),
                })
        except (json.JSONDecodeError, KeyError) as e:
            print(f"  [SKIP] {fpath.name}: {e}")

    results.sort(key=lambda x: -x["f1"])
    return results


def main():
    print("Benchmark: 120-Model Ranking")
    print("  Loading benchmark results...")
    results = load_benchmark_results()
    print(f"  Found {len(results)} models with valid F1 scores")

    if not results:
        print("  [FAIL] No benchmark results yet. Run batch_model_benchmark.py first.")
        return

    # Save aggregated results
    agg_path = OUTPUT_DIR / "model_benchmark_full.json"
    with open(agg_path, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)
    print(f"  [OK] {agg_path}")

    f1_scores = [r["f1"] for r in results]
    model_names = [r["model"][:40] for r in results]

    print(f"\n  Summary:")
    print(f"    Total models: {len(results)}")
    print(f"    Mean F1:      {np.mean(f1_scores):.4f}")
    print(f"    Median F1:    {np.median(f1_scores):.4f}")
    print(f"    Std F1:       {np.std(f1_scores):.4f}")
    print(f"    Max F1:       {max(f1_scores):.4f}")
    print(f"    Min F1:       {min(f1_scores):.4f}")

    # Top 10
    print(f"\n  Top 10 Models:")
    for i, r in enumerate(results[:10], 1):
        print(f"    {i:2d}. {r['model'][:55]:55s} F1={r['f1']:.4f}")

    # Bottom 5
    print(f"\n  Bottom 5 Models:")
    for i, r in enumerate(results[-5:], len(results) - 4):
        print(f"    {i:2d}. {r['model'][:55]:55s} F1={r['f1']:.4f}")

    # ── Figure 1: Histogram with distribution ──
    fig, axes = plt.subplots(1, 2, figsize=(12, 4.5))

    ax = axes[0]
    n_bins = min(30, max(10, len(results) // 4))
    ax.hist(f1_scores, bins=n_bins, color="#2563eb", alpha=0.7, edgecolor="white")
    ax.axvline(np.mean(f1_scores), color="#ea580c", linestyle="--", label=f"Mean={np.mean(f1_scores):.3f}")
    ax.axvline(np.median(f1_scores), color="#16a34a", linestyle=":", label=f"Median={np.median(f1_scores):.3f}")

    # Highlight qwen models
    for r in results:
        if "qwen" in r["model"].lower():
            ax.axvline(r["f1"], color="#9333ea", linestyle="-.", alpha=0.5)

    ax.set_xlabel("F1 Score", fontsize=11)
    ax.set_ylabel("Number of Models", fontsize=11)
    ax.set_title(f"Distribution of F1 Scores\n({len(results)} models)", fontsize=11)
    ax.legend(fontsize=8)
    ax.set_xlim(0, 1.0)

    # Annotations
    qwen_results = [r for r in results if "qwen" in r["model"].lower()]
    if qwen_results:
        qwen_text = "\n".join(f"  {r['model']}: F1={r['f1']:.4f}" for r in qwen_results[:3])
        ax.text(0.97, 0.97, f"Qwen models:\n{qwen_text}",
                transform=ax.transAxes, ha="right", va="top",
                fontsize=7, color="#555",
                bbox=dict(boxstyle="round,pad=0.3", facecolor="#f8f9fa", edgecolor="#ddd"))

    # ── Figure 2: Top-N model bar chart ──
    ax = axes[1]
    n_show = min(20, len(results))
    top = results[:n_show]
    names = [r["model"][:35] for r in top]
    f1s = [r["f1"] for r in top]
    colors = ["#2563eb" if "qwen" in r["model"].lower() else "#6b7280" for r in top]

    y_pos = np.arange(len(names))
    bars = ax.barh(y_pos, f1s, 0.6, color=colors, alpha=0.85)
    for bar, val in zip(bars, f1s):
        ax.text(bar.get_width() + 0.005, bar.get_y() + bar.get_height() / 2,
                f"{val:.4f}", va="center", fontsize=7)

    ax.set_yticks(y_pos)
    ax.set_yticklabels(names, fontsize=6.5)
    ax.invert_yaxis()
    ax.set_xlabel("F1 Score", fontsize=11)
    ax.set_title(f"Top {n_show} Models", fontsize=11)
    ax.set_xlim(0, 1.05)

    # Legend for color
    from matplotlib.patches import Patch
    legend_elements = [
        Patch(facecolor="#2563eb", alpha=0.85, label="Qwen models"),
        Patch(facecolor="#6b7280", alpha=0.85, label="Other models"),
    ]
    ax.legend(handles=legend_elements, fontsize=8, loc="lower right")

    plt.tight_layout()
    path = OUTPUT_DIR / "fig_benchmark_120.png"
    fig.savefig(path, dpi=300)
    print(f"\n  [OK] {path}")
    plt.close(fig)

    # CSV: Full ranking
    csv_path = OUTPUT_DIR / "fig_benchmark_120_data.csv"
    with open(csv_path, "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["rank", "model", "f1", "precision", "recall", "valid", "total", "failed"])
        for i, r in enumerate(results, 1):
            w.writerow([i, r["model"], r["f1"], r["precision"], r["recall"],
                       r["valid"], r["total"], r["failed"]])
    print(f"  [OK] {csv_path}")


if __name__ == "__main__":
    main()
